package kr.co.serendipity.model.dto;

public class TravelInfoDTO {
	private int info_num;
	private String local_code;
	private String bus_fee;
	private String taxi_fee;
	private String famous_food;
	private String attraction;
	private String airport;
	private String img;
	
	public int getInfo_num() {
		return info_num;
	}
	public void setInfo_num(int info_num) {
		this.info_num = info_num;
	}
	public String getLocal_code() {
		return local_code;
	}
	public void setLocal_code(String local_code) {
		this.local_code = local_code;
	}
	public String getBus_fee() {
		return bus_fee;
	}
	public void setBus_fee(String bus_fee) {
		this.bus_fee = bus_fee;
	}
	public String getTaxi_fee() {
		return taxi_fee;
	}
	public void setTaxi_fee(String taxi_fee) {
		this.taxi_fee = taxi_fee;
	}
	public String getFamous_food() {
		return famous_food;
	}
	public void setFamous_food(String famous_food) {
		this.famous_food = famous_food;
	}
	public String getAttraction() {
		return attraction;
	}
	public void setAttraction(String attraction) {
		this.attraction = attraction;
	}
	public String getAirport() {
		return airport;
	}
	public void setAirport(String airport) {
		this.airport = airport;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	
	
}
